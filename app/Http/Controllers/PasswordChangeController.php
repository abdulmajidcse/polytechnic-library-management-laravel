    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $userPassword = Auth::user()->password;
        $oldPassword = $request->oldPassword;
        $newPassword = $request->newPassword;
        $confirmPassword = $request->confirmPassword;
        if (Hash::check($oldPassword, $userPassword)) {
            if ($newPassword == $confirmPassword) {
                
                $user = User::find(Auth::user()->id);
                $user->password = Hash::make($newPassword);
                $user->save();
                Auth::logout();
                return redirect()->route('login')->with('successMsg', 'Successfully password changed! Now log in.');

            } else {
                return redirect()->back()->with('errorMsg', 'New password doesn\'t match with confirm password!');
            }
        } else {
            return redirect()->back()->with('errorMsg', 'Old password doesn\'t match!');
        }
    }

